const ingredients = [
  'Potatoes',
  'Mushrooms',
  'Garlic',
  'Tomatos',
  'Herbs',
  'Condiments',
];

const selector = document.getElementById('ingredients'); 
const arr =[];
ingredients.forEach(item => {
  const child = document.createElement('li');
  child.className = "item";
  child.textContent = item;
  arr.push(child);
});
selector.append(...arr);

